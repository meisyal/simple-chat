    public void SendTo(String To, String Text){
        KIJChatting temp2;
        for(i=0; i<counter; i++){
            if(To.equals(User.get(i))){
                temp2=Chat.get(i);
                temp2.receive(To, Text);
            }
        }
    }
    
    public void sendDisconnect(){
        try{
            writer.println("DCON:"+username);
            writer.flush();
        }catch(Exception ex){
            ActivityTextArea.append("Tidak bisa mengirimkan Pesan Disconnect. \n");
        }
    }
    
    public void Disconnect(){
        try{
            ActivityTextArea.append("Disconnected.\n");
            sock.close();            
        }catch(Exception ex){
            ActivityTextArea.append("Gagal Untuk Disconnect. \n");
        }
        this.ClearListUser();
    }

    
